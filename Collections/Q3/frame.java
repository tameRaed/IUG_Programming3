    private void write(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_write
        try {
            accountNumber.getText();
            accountBalance.getText();
            if (accountNumber.getText().equals("") || accountName.getText().equals("")
                    || accountBalance.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please fill in all fields",
                        "ERROR!!", JOptionPane.WARNING_MESSAGE);
            } else {
                FileOutputStream fileOutputStream = null;
                Account account = new Account(Integer.parseInt(accountNumber.getText()), accountName
                        .getText(), Float.parseFloat(accountBalance.getText()));
                listAccount.add(account);
                try {
                    fileOutputStream = new FileOutputStream(file);
                    ObjectOutputStream objectOutputStream = new ObjectOutputStream(fileOutputStream);
                    objectOutputStream.writeObject(listAccount);
                    objectOutputStream.flush();
                    objectOutputStream.close();
                    JOptionPane.showMessageDialog(null, "Account successfully added",
                            "", JOptionPane.INFORMATION_MESSAGE);
                    accountNumber.setText("");
                    accountName.setText("");
                    accountBalance.setText("");
                } catch (IOException ex) {
                    ex.printStackTrace();
                } finally {
                    try {
                        fileOutputStream.close();
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter the correct data type",
                    "ERROR!!", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_write

