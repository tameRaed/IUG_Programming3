    private void read(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_read
        String result = "";
        ArrayList<Account> newList;
        try {
            FileInputStream fileInputStream = new FileInputStream(file);
            if (fileInputStream.available() != 0) {
                ObjectInputStream objectInputStream = new ObjectInputStream(fileInputStream);
                newList = (ArrayList<Account>) objectInputStream.readObject();
                for (Account account : newList) {
                    result += account.toString() + "\n----------------------\n";
                }

                sortDesending();
                printListContent();
                area.setText(result);

            } else {
                JOptionPane.showMessageDialog(null, "File Empty!!!",
                        "ERROR!!", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            area.setText(result);
        }
    }//GEN-LAST:event_read

    public void sortDesending() {
        listAccount.sort(new Comparator<Account>() {
            @Override
            public int compare(Account o1, Account o2) {
                if (o2.getAccBalance() > o1.getAccBalance()) {
                    return 1;
                } else if (o2.getAccBalance() < o1.getAccBalance()) {
                    return -1;
                }
                return 0;
            }
        });
    }

    public void printListContent() {
        for (int i = 0; i < listAccount.size(); i++) {
            System.out.printf("%-10d %-10s %8.2f\n",
                    listAccount.get(i).getAccNo(),
                    listAccount.get(i).getAccName(),
                    listAccount.get(i).getAccBalance());
        }
    }

